function aa =PowerMethod(A) % Yλοποιεί την μέθοδο των δυνάμεων και επιστρέφει το ιδιοδιάνυσμα που αντίστοιχει 
format long;                % στην μεγαλύτερη ιδιοτιμή του πίνακα.
[m,n]=size(A);
b=A(1:m,n);
for i=1:20
    b=A*b;
    l=1/b(1);
    b=l*b;
end
b=(1/l)*b;
aa=b;   % το ιδιοδιάνυσμα που αντίστοιχει  στην μεγαλύτερη ιδιοτιμή του πίνακα.
end

